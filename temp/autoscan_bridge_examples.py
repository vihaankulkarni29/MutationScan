"""
AutoScanBridge Usage Examples

This script demonstrates how to use the AutoScanBridge module to connect
MutationScan's population-level mutation data to AutoScan's 3D biophysics engine.

Typical Workflow:
1. MutationScan identifies epistatic mutation network (e.g., residues [10, 174, 970])
2. AutoScanBridge generates AutoScan-compatible configuration
3. AutoScan runs comparative docking (WT vs mutant)
4. Results are parsed and returned as ΔΔG values
"""

from pathlib import Path
from mutation_scan.biophysics import AutoScanBridge


# Example 1: Basic usage with default AutoScan path
def example_basic_docking():
    """
    Run comparative docking for an identified epistatic network.
    
    Scenario: We detected mutations at residues 10, 174, and 970 on chain A
    of PDB 7cz9 (target protein for ciprofloxacin binding).
    """
    
    # Initialize the bridge
    bridge = AutoScanBridge(autoscan_path="python -m autoscan.main")
    
    # Define the epistatic network
    pdb_id = "7cz9"
    chain = "A"
    residues = [10, 174, 970]
    
    # Ciprofloxacin SMILES (example)
    ciprofloxacin_smiles = "O=C(O)C1=CN=C(N2CC3C=CC(N4CCNCC4)=C3C2)C(N)=C1F"
    
    # Run docking
    results = bridge.run_comparative_docking(
        pdb_id=pdb_id,
        chain=chain,
        residues=residues,
        ligand_smiles=ciprofloxacin_smiles,
        output_dir=Path("data/results/autoscan/")
    )
    
    if results:
        print(f"\n{'='*60}")
        print(f"DOCKING RESULTS for {pdb_id} Chain {chain}")
        print(f"{'='*60}")
        print(f"Residues: {residues}")
        print(f"Ligand: Ciprofloxacin")
        print(f"WT Affinity: {results['wt_affinity']:.2f} kcal/mol")
        print(f"Mutant Affinity: {results['mutant_affinity']:.2f} kcal/mol")
        print(f"ΔΔG: {results['delta_delta_g']:.2f} kcal/mol")
        
        # Interpretation
        if results['delta_delta_g'] < 0:
            print(f"\n✓ Mutation IMPROVES binding (ΔΔG = {results['delta_delta_g']:.2f} kcal/mol)")
        elif results['delta_delta_g'] > 0:
            print(f"\n✗ Mutation WEAKENS binding (ΔΔG = {results['delta_delta_g']:.2f} kcal/mol)")
        else:
            print(f"\n→ No significant change in binding")
    else:
        print("Error: Docking failed. Check logs above.")


# Example 2: Batch processing multiple epistatic networks
def example_batch_processing():
    """
    Process multiple epistatic networks identified from different genomes.
    
    Use case: Population-level analysis where we have several statistically
    significant mutation clusters to analyze.
    """
    
    bridge = AutoScanBridge()
    
    # Define multiple networks found via MutationScan
    networks = [
        {
            "pdb_id": "7cz9",
            "chain": "A",
            "residues": [10, 174, 970],
            "ligand": "C1=C[C@H]2[C@H]3[C@H](C=C2C=C1)C[C@@H](C3)N(CCN)C4=CC=C(F)C=C4",  # Cipro
            "name": "Network 1: Triple mutant"
        },
        {
            "pdb_id": "7cz9",
            "chain": "A",
            "residues": [42, 156],
            "ligand": "C1=C[C@H]2[C@H]3[C@H](C=C2C=C1)C[C@@H](C3)N(CCN)C4=CC=C(F)C=C4",
            "name": "Network 2: Double mutant"
        }
    ]
    
    results_summary = []
    
    for network in networks:
        print(f"\nProcessing: {network['name']}")
        
        result = bridge.run_comparative_docking(
            pdb_id=network['pdb_id'],
            chain=network['chain'],
            residues=network['residues'],
            ligand_smiles=network['ligand'],
            output_dir=Path(f"data/results/autoscan/{network['name'].replace(' ', '_')}/")
        )
        
        if result:
            results_summary.append({
                "network": network['name'],
                "residues": network['residues'],
                "delta_delta_g": result['delta_delta_g']
            })
    
    # Print summary
    if results_summary:
        print(f"\n{'='*60}")
        print("BATCH PROCESSING SUMMARY")
        print(f"{'='*60}")
        
        # Sort by ΔΔG (most improved binding first)
        results_summary.sort(key=lambda x: x['delta_delta_g'])
        
        for i, result in enumerate(results_summary, 1):
            print(f"{i}. {result['network']}")
            print(f"   Residues: {result['residues']}")
            print(f"   ΔΔG: {result['delta_delta_g']:.2f} kcal/mol")


# Example 3: Integration with MutationScan's epistasis analysis
def example_integration_with_mutationscan():
    """
    Show how AutoScanBridge integrates with MutationScan's epistasis output.
    
    Workflow:
    1. MutationScan detects mutations → mutation_report.csv
    2. Epistasis analysis identifies networks → epistasis_input.json
    3. AutoScanBridge calculates ΔΔG for each network
    4. Results guide antimicrobial stewardship decisions
    """
    
    import json
    
    # Load epistasis input (generated by MutationScan)
    epistasis_file = Path("temp/epistasis_input.json")
    
    if not epistasis_file.exists():
        print(f"Epistasis file not found: {epistasis_file}")
        return
    
    with open(epistasis_file, 'r') as f:
        epistasis_data = json.load(f)
    
    bridge = AutoScanBridge()
    
    # Extract networks from epistasis data
    results_by_gene = {}
    
    for genome in epistasis_data.get("genomes", [])[:1]:  # Example: process first genome
        for gene, mutations in genome.items():
            if gene not in results_by_gene:
                results_by_gene[gene] = []
            
            # Extract position numbers from mutation format (e.g., "R10K" -> position 10)
            positions = []
            for mutation in mutations:
                # Parse mutation notation (e.g., "R10K" -> position 10)
                import re
                match = re.search(r'\d+', mutation)
                if match:
                    positions.append(int(match.group()))
            
            if positions:
                # Map gene to PDB (example mapping - adjust as needed)
                pdb_mapping = {
                    "oqxA": "7cz9",
                    "oqxB": "7cz9"
                }
                
                pdb_id = pdb_mapping.get(gene, "7cz9")
                chain = "A"
                
                print(f"\nAnalyzing {gene} with mutations at positions: {positions}")
                
                result = bridge.run_comparative_docking(
                    pdb_id=pdb_id,
                    chain=chain,
                    residues=positions,
                    ligand_smiles="O=C(O)C1=CN=C(N2CC3C=CC(N4CCNCC4)=C3C2)C(N)=C1F",
                    output_dir=Path("data/results/autoscan/")
                )
                
                if result:
                    results_by_gene[gene].append({
                        "positions": positions,
                        "delta_delta_g": result['delta_delta_g']
                    })
    
    # Print summary by gene
    if results_by_gene:
        print(f"\n{'='*60}")
        print("INTEGRATION RESULTS: Mutations → Binding Affinity")
        print(f"{'='*60}")
        for gene, results in results_by_gene.items():
            print(f"\nGene: {gene}")
            for result in results:
                print(f"  Positions {result['positions']}: ΔΔG = {result['delta_delta_g']:.2f} kcal/mol")


